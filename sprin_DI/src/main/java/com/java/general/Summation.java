package com.java.general;

public class Summation {

	private int a;
	private int b;

	public int sum() {
		int result = 0;
		result = a + b;
		return result;
	}
	
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	

	
}
